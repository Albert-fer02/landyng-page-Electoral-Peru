export function calculatePollAverage(polls) {
  const { ipsos, datum, cpi } = METHODOLOGY.pollWeight;
  if (!polls.ipsos && !polls.datum && !polls.cpi) return null;
  return (
    ((polls.ipsos || 0) * ipsos +
      (polls.datum || 0) * datum +
      (polls.cpi || 0) * cpi) /
    1
  ).toFixed(1);
}

